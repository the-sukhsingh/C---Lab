#include <iostream>
using namespace std;

int main()
{
    int a = 10;
    int b = 20;
    cout << "Operators in C++" << endl;
    cout << "a = " << a << endl;
    cout << "b = " << b << endl;
    cout << "\vBinary operators" << endl;
    cout << "\vArithmetic Operators" << endl;
    cout << "a + b = " << a + b << endl;
    cout << "a - b = " << a - b << endl;
    cout << "a * b = " << a * b << endl;
    cout << "a / b = " << a / b << endl;
    cout << "a % b = " << a % b << endl;
    cout << "\vUnary Operators" << endl;
    cout << "a = " << a << endl;
    cout << "b = " << b << endl;
    cout << "a++ = " << a++ << endl;
    cout << "a-- = " << a-- << endl;
    cout << "++a = " << ++a << endl;
    cout << "--a = " << --a << endl;
    cout << "\vAssignment Operators" << endl;
    cout << "a = " << a << endl;
    cout << "b = " << b << endl;
    cout << "a += b = " << (a += b) << endl;
    cout << "a -= b = " << (a -= b) << endl;
    cout << "a *= b = " << (a *= b) << endl;
    cout << "a /= b = " << (a /= b) << endl;
    cout << "a %= b = " << (a %= b) << endl;
    cout << "\vRelational Operators" << endl;
    cout << "a = " << a << endl;
    cout << "b = " << b << endl;
    cout << "a == b = " << (a == b) << endl;
    cout << "a != b = " << (a != b) << endl;
    cout << "a > b = " << (a > b) << endl;
    cout << "a < b = " << (a < b) << endl;
    cout << "a >= b = " << (a >= b) << endl;
    cout << "a <= b = " << (a <= b) << endl;
    cout << "\vLogical Operators" << endl;
    cout << "a = " << a << endl;
    cout << "b = " << b << endl;
    cout << "a && b = " << (a && b) << endl;
    cout << "a || b = " << (a || b) << endl;
    cout << "!a = " << !a << endl;
    cout << "!b = " << !b << endl;
    cout << "\vBitwise Operators" << endl;
    cout << "a = " << a << endl;
    cout << "b = " << b << endl;
    cout << "a & b = " << (a & b) << endl;
    cout << "a | b = " << (a | b) << endl;
    cout << "a ^ b = " << (a ^ b) << endl;
    cout << "~a = " << ~a << endl;
    cout << "~b = " << ~b << endl;
    cout << "\vShift Operators" << endl;
    cout << "a = " << a << endl;
    cout << "b = " << b << endl;
    cout << "a << 1 = " << (a << 1) << endl;
    cout << "a >> 1 = " << (a >> 1) << endl;

    return 0;
}